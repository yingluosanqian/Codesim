#include<iostream>
using namespace std;
int i,j,k,tsy;
int main(){
	int m,n,count,num,sum;
	cin>>m;
	string s;
	for(j=0;j<m;j++){
	count=0,num=0,sum=0;
	cin>>n;
	cin>>s;
	for(i=0;i<n;i++){
		if(s[i]=='M')
		sum++;
	}
	if(sum!=n/3){
		cout<<"NO"<<endl;
		continue;
	}
	for(i=0;i<n;i++){
		if(num==sum)
		break;
	    if(s[i]=='T'){
	    	count++;
	    	continue;
		}
		if(s[i]=='M'){
			while(s[i]=='M'){
				num++;
				i++;
			}
			i--;
			if(count<num){
			tsy=1;
			break;
		}
		}	
	}
	count=0,num=0;
	for(i=n-1;i>=0;i--){
		if(num==sum)
		break;
	    if(s[i]=='T')
	    count++;
		if(s[i]=='M'){
			while(s[i]=='M'){
				num++;
				i--;
			}
			i++;
			if(count<num){
			tsy=1;
			break;
		}
		}	
	}
	if(tsy==0)
	cout<<"YES"<<endl;
    else
    cout<<"NO"<<endl;
	tsy=0;
	}
} 
